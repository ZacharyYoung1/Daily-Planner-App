var storageInput1 = document.querySelector('.storage');
var button1 = document.querySelector('.button');
var storedInput1= localStorage.getItem('textinput1')

if(storageInput1) {
storageInput1.textContent = storedInput1

}

storageInput1.addEventListener('input', letter => {
storageInput1.textContent = letter.target.value

});

var saveLocal = () => {
localStorage.setItem('textinput1', storageInput1.textContent)

}
button1.addEventListener('click', saveLocal)

